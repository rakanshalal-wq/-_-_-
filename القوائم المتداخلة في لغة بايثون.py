# القوائم المتداخلة في لغة بايثون
# اسمها بالإنجليزي: nested lists
# هذا المشروع عن "الكتب" وكيف نخزنها داخل صندوقين (قائمتين) داخل صندوق واحد (قائمة كبيرة).
# الفكرة: نتعلم كيف ننشئ الصندوق، وكيف نستدعي الصناديق الداخلية، وكيف نصل لكل كتاب.

# هنا ننشئ المتغير الأساسي باسم books
# هذا المتغير يمثل الصندوق الكبير (القائمة الرئيسية)
# داخل هذا الصندوق يوجد صندوقان أصغر (قائمتان)، وكل قائمة فيها كتب.
# نستخدم الشكل التالي:
# [[يكون داخلها البيانات], [يكون داخلها البيانات]]
books = [['book1', 'book2'], ['book3', 'book4']]

# استدعاء الصندوق الأول ومحتواه
# books[0] تعني: الصندوق الأول داخل الصندوق الكبير
print(books[0])

# استدعاء الصندوق الثاني ومحتواه
# books[1] تعني: الصندوق الثاني داخل الصندوق الكبير
print(books[1])

# استدعاء الصندوقين معًا في نفس السطر
# هنا نطبع الصندوق الأول ثم الصندوق الثاني
print(books[0], books[1])

# الصندوق الأول، الكتاب الأول
# books[0] = الصندوق الأول
# books[0][0] = أول كتاب داخل الصندوق الأول
print(books[0][0])

# الصندوق الأول، الكتاب الثاني
# books[0][1] = ثاني كتاب داخل الصندوق الأول
print(books[0][1])

# الصندوق الثاني، الكتاب الأول
# books[1][0] = أول كتاب داخل الصندوق الثاني
print(books[1][0])

# الصندوق الثاني، الكتاب الثاني
# books[1][1] = ثاني كتاب داخل الصندوق الثاني
print(books[1][1])

# الصندوق الأول الكتاب الأول + الصندوق الثاني الكتاب الأول في كود واحد
# هنا نطبع book1 من الصندوق الأول و book3 من الصندوق الثاني معًا
print(books[0][0], books[1][0])

# الصندوق الأول الكتاب الثاني + الصندوق الثاني الكتاب الثاني في كود واحد
# هنا نطبع book2 من الصندوق الأول و book4 من الصندوق الثاني معًا
print(books[0][1], books[1][1])

# إلى هنا تعلمنا كيف نستدعي القوائم المتداخلة (صناديق داخل الصندوق الكبير).
# الآن ننتقل لفكرة الإضافة والحذف على نفس الصندوق.

###################### إضافة صناديق جديدة (append) #########################
# append: دالة نستخدمها لإضافة عنصر جديد في نهاية القائمة.
# هنا نستخدمها لإضافة صندوق ثالث جديد يحتوي على book5 و book6.
# نبدأ باسم المتغير (books) ثم نقطة ثم append ثم نمرر البيانات الجديدة داخل أقواس.
books.append(['book5', 'book6'])

# طباعة المتغير بعد الإضافة حتى نرى الشكل الجديد للصندوق
print(books)

################### ملاحظة عن append ###################
# append دائمًا تضيف في "آخر" القائمة.
# إذا أردنا التحكم في مكان الإضافة نستخدم دالة أخرى اسمها insert.

############## استخدام insert لتحديد مكان الإضافة ##############
# insert تشتغل بهذا الشكل:
# اسم القائمة.دالة insert(رقم_المكان, القيمة_التي_نريد_إضافتها)
# مثال توضيحي (غير منفذ):
# books.insert(0, 'book11')
# يعني نضيف 'book11' في بداية القائمة (المكان رقم 0).

# في هذا المشروع نطبق insert عمليًا:
# هنا نضيف 'book00' في المكان رقم 0
# أي أن 'book00' سيكون في بداية القائمة (قبل كل الصناديق).
books.insert(0, 'book00')

# طباعة المتغير بعد استخدام insert
print(books)

# الآن نضيف قيمة أخرى في مكان مختلف
# نضيف 'book44' في المكان رقم 3
# هذا يعني أنها ستوضع في الفهرس 3 داخل القائمة الرئيسية.
books.insert(3, 'book44')

# طباعة المتغير بعد الإضافة الثانية باستخدام insert
print(books)

############## الحذف من القائمة باستخدام remove ##############
# إذا أردنا حذف قيمة من القائمة نستخدم دالة remove.
# remove تحذف "القيمة نفسها" وليس رقم الفهرس.
# ملاحظة مهمة: يجب أن تكون القيمة موجودة فعلاً داخل القائمة حتى لا يحدث خطأ.

# هنا مثال على حذف book1 من أحد الصناديق الداخلية:
# أولاً: books[1] يشير إلى الصندوق الثاني (بعد التعديلات السابقة)
# ثانياً: داخل هذا الصندوق توجد القيمة 'book1' في بعض الحالات التعليمية.
# الفكرة التعليمية هنا: نوضح طريقة كتابة remove على صندوق داخلي:
# books[رقم_الصندوق].remove('اسم_العنصر')
# مثالنا:
# نحذف 'book1' من الصندوق الثاني (في الكود التعليمي الأصلي).
# يمكنك تعديل القيمة حسب ما هو موجود فعليًا.
# في تطبيقنا هنا سنفترض أن الصندوق books[1] يحتوي على 'book1' ونطبق remove عليها.
# إذا أردت أن تجعل المثال مطابقًا 100٪ للبيانات الفعلية،
# يمكنك حذف 'book3' بدلاً من 'book1' لأن 'book3' موجود فعلاً.
# مثال آمن يعمل على بياناتنا الحالية:
books[2].remove('book3')

# طباعة المتغير بعد الحذف لرؤية الشكل النهائي للقائمة
print(books)
